export function localizedResultText(value: string, locale: Locale): string {
  if (locale === 'ru') return value;
  const currencyByLocale: Partial<Record<Locale, string>> = {
    en: '$',
    pl: 'zł',
    ro: 'lei',
    id: 'Rp',
    tr: '₺',
    vi: '₫',
    cs: 'Kč',
    uk: '₴',
    hu: 'Ft',
  };
  const currency = currencyByLocale[locale] ?? '€';
  return value
    .replaceAll('₽', currency)
    .replaceAll('ккал', 'kcal')
    .replaceAll('кг', 'kg')
    .replaceAll('см', 'cm')
    .replaceAll('км/ч', 'km/h')
    .replaceAll('мес.', 'mo.')
    .replaceAll('лет', 'years')
    .replaceAll('дн.', 'days')
    .replaceAll('шт.', 'pcs.')
    .replaceAll('л', 'L')
    .replaceAll('демонстрационный', 'demo');
}

