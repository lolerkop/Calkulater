export function localizedResultText(value: string, locale: Locale): string {
  if (locale === 'ru') return value;
  const currencyByLocale: Partial<Record<Locale, string>> = {
    en: '$',
    pl: 'zł',
    ro: 'lei',
    id: 'Rp',
    tr: '₺',
    vi: '₫',
    cs: 'Kč',
    uk: '₴',
    hu: 'Ft',
  };
  const currency = currencyByLocale[locale] ?? '€';
  const phrases = resultValueMap[locale] ?? resultValueMap.en;
  let localized = phrases[value] ?? value;

  for (const [source, target] of Object.entries(phrases)) {
    if (localized.includes(source)) localized = localized.replaceAll(source, target);
  }

  if (locale === 'en') {
    localized = localized
      .replace(/(\d+) (?:года|год|лет)/g, '$1 years')
      .replace(/(\d+) (?:месяцев|месяца|месяц)/g, '$1 months')
      .replace(/(\d+) (?:дней|дня|день)/g, '$1 days');
  } else if (locale === 'uk') {
    localized = localized
      .replace(/(\d+) (?:года|год|лет)/g, '$1 років')
      .replace(/(\d+) (?:месяцев|месяца|месяц)/g, '$1 місяців')
      .replace(/(\d+) (?:дней|дня|день)/g, '$1 днів');
  }

  const units = locale === 'uk'
    ? { month: 'міс.', year: 'років', day: 'дн.', piece: 'шт.', liter: 'л', gram: 'г', kg: 'кг', cm: 'см', kcal: 'ккал', pace: '/км' }
    : { month: 'mo.', year: 'years', day: 'days', piece: 'pcs.', liter: 'L', gram: 'g', kg: 'kg', cm: 'cm', kcal: 'kcal', pace: '/km' };

  return localized
    .replaceAll('₽', currency)
    .replace(/ккал/g, units.kcal)
    .replace(/км\/ч/g, 'km/h')
    .replace(/\/км/g, units.pace)
    .replace(/\/миля/g, locale === 'uk' ? '/миля' : '/mi')
    .replace(/ км(?=$|\s|[),])/g, locale === 'uk' ? ' км' : ' km')
    .replace(/м²/g, 'm²')
    .replace(/ м(?=$|\s|[),×])/g, locale === 'uk' ? ' м' : ' m')
    .replace(/ кг(?=$|\s|[),–])/g, ` ${units.kg}`)
    .replace(/ см(?=$|\s|[),])/g, ` ${units.cm}`)
    .replace(/ мес\./g, ` ${units.month}`)
    .replace(/ лет(?=$|\s|[),])/g, ` ${units.year}`)
    .replace(/ дн\./g, ` ${units.day}`)
    .replace(/ шт\./g, ` ${units.piece}`)
    .replace(/ л(?=$|\s|[),×])/g, ` ${units.liter}`)
    .replace(/ г(?=$|\s|[),])/g, ` ${units.gram}`);
}

